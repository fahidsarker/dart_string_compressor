Numerous phytochemicals are present in mango peel and pulp, such as the triterpene lupeol.[36] Mango peel pigments under study include carotenoids, such as the provitamin A compound, beta-carotene, lutein and alpha-carotene,[37][38] and polyphenols, such as quercetin, kaempferol, gallic acid, caffeic acid, catechins and tannins.[39][40] Mango contains a unique xanthonoid called mangiferin.[41]

Phytochemical and nutrient content appears to vary across mango cultivars.[42] Up to 25 different carotenoids have been isolated from mango pulp, the densest of which was beta-carotene, which accounts for the yellow-orange pigmentation of most mango cultivars.[43] Mango leaves also have significant polyphenol content, including xanthonoids, mangiferin and gallic acid.[44]
Flavor

The flavor of mango fruits is conferred by several volatile organic chemicals mainly belonging to terpene, furanone, lactone, and ester classes. Different varieties or cultivars of mangoes can have flavors made up of different volatile chemicals or the same volatile chemicals in different quantities.[45] In general, New World mango cultivars are characterized by the dominance of δ-3-carene, a monoterpene flavorant; whereas, high concentration of other monoterpenes such as (Z)-ocimene and myrcene, as well as the presence of lactones and furanones, is the unique feature of Old World cultivars.[46][47][48] In India, 'Alphonso' is one of the most popular cultivars. In 'Alphonso' mango, the lactones and furanones are synthesized during ripening, whereas terpenes and the other flavorants are present in both the developing (immature) and ripening fruits.[49][50][51] Ethylene, a ripening-related hormone well known to be involved in ripening of mango fruits, causes changes in the flavor composition of mango fruits upon exogenous application, as well.[52][53] In contrast to the huge amount of information available on the chemical composition of mango flavor, the biosynthesis of these chemicals has not been studied in depth; only a handful of genes encoding the enzymes of flavor biosynthetic pathways have been characterized to date.[54][55][56][57]
Toxicity

Contact with oils in mango leaves, stems, sap, and skin can cause dermatitis and anaphylaxis in susceptible individuals.[4][29][58] Those with a history of contact dermatitis induced by urushiol (an allergen found in poison ivy, poison oak, or poison sumac) may be most at risk for mango contact dermatitis.[59] Other mango compounds potentially responsible for dermatitis or allergic reactions include mangiferin.[4] Cross-reactions may occur between mango allergens and urushiol.[60] Sensitized individuals may not be able to eat peeled mangos or drink mango juice safely.[4]

When mango trees are flowering in spring, local people with allergies may experience breathing difficulty, itching of the eyes, or facial swelling, even before flower pollen becomes airborne.[4] In this case, the irritant is likely to be the vaporized essential oil from flowers.[4] During the primary ripening season of mangoes, contact with mango plant parts – primarily sap, leaves, and fruit skin[4] – is the most common cause of plant dermatitis in Hawaii.[61] 
    
