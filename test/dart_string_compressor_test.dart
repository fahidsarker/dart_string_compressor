Mangoes are used in many cuisines. Sour, unripe mangoes are used in chutneys (i.e., mango chutney), pickles, daals and other side dishes in Indian cuisine. A summer drink called aam panna is made with mangoes. Mango pulp made into jelly or cooked with red gram dhal and green chilies may be served with cooked rice. Mango lassi is consumed throughout South Asia, prepared by mixing ripe mangoes or mango pulp with buttermilk and sugar. Ripe mangoes are also used to make curries. Aamras is a thick juice made of mangoes with sugar or milk and is consumed with chapatis or pooris. The pulp from ripe mangoes is also used to make jam called mangada. Andhra aavakaaya is a pickle made from raw, unripe, pulpy, and sour mango mixed with chili powder, fenugreek seeds, mustard powder, salt, and groundnut oil. Mango is also used to make dahl and chunda (a sweet and spicy, grated mango delicacy).

Mangoes are used to make murabba (fruit preserves), muramba (a sweet, grated mango delicacy), amchur (dried and powdered unripe mango), and pickles, including a spicy mustard-oil pickle and alcohol. Ripe mangoes are cut into thin layers, desiccated, folded and then cut. The fruit is also added to cereal products such as muesli and oat granola.

Mango is used to make juices, smoothies, ice cream, fruit bars, raspados, aguas frescas, pies, and sweet chili sauce, or mixed with chamoy, a sweet and spicy chili paste. In Central America, mango is either eaten green, mixed with salt, vinegar, black pepper, and hot sauce, or ripe in various forms.

Pieces of mango can be mashed and used as a topping on ice cream or blended with milk and ice as milkshakes. Sweet glutinous rice is flavored with coconut, then served with sliced mango as mango sticky rice. In other parts of Southeast Asia, mangoes are pickled with fish sauce and rice vinegar. Green mangoes can be used in mango salad with fish sauce and dried shrimp. Mango with condensed milk may be used as a topping for shaved ice.

Raw green mangoes can be sliced and eaten like a salad.[30] In most parts of Southeast Asia, they are commonly eaten with fish sauce, vinegar, soy sauce, or with a dash of salt (plain or spicy) – a combination usually known as "mango salad" in English.[31]
