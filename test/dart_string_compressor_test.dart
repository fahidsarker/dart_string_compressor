Taxonomy
'Carabao', a typical "Southeast Asian type" polyembryonic mango cultivar
'Langra', a typical "Indian type" monoembryonic mango cultivar

Mangoes originated from the region between northwestern Myanmar, Bangladesh, and northeastern India.[1][2] The mango is considered an evolutionary anachronism, whereby seed dispersal was once accomplished by a now-extinct evolutionary forager, such as a megafauna mammal.[13]

From their center of origin, mangoes diverged into two genetically distinct populations: the subtropical Indian group and the tropical Southeast Asian group. The Indian group is characterized by having monoembryonic fruits, while polyembryonic fruits characterize the Southeast Asian group.[1][2]

It was previously believed that mangoes originated from a single domestication event in South Asia before being spread to Southeast Asia, but a 2019 study found no evidence of a center of diversity in India. Instead, it identified a higher unique genetic diversity in Southeast Asian cultivars than in Indian cultivars, indicating that mangoes may have originally been domesticated first in Southeast Asia before being introduced to South Asia. However, the authors also cautioned that the diversity in Southeast Asian mangoes might be the result of other reasons (like interspecific hybridization with other Mangifera species native to the Malesian ecoregion). Nevertheless, the existence of two distinct genetic populations also identified by the study indicates that the domestication of the mango is more complex than previously assumed and would at least indicate multiple domestication events in Southeast Asia and South Asia.[1][2]
Cultivars
Main article: List of mango cultivars

There are many hundreds of named mango cultivars. In mango orchards, several cultivars are often grown to improve pollination. Many desired cultivars are monoembryonic and must be propagated by grafting, or they do not breed true. A common monoembryonic cultivar is 'Alphonso', an important export product, considered "the king of mangoes."[14]

Cultivars that excel in one climate may fail elsewhere. For example, Indian cultivars such as 'Julie,' a prolific cultivar in Jamaica, require annual fungicide treatments to escape the lethal fungal disease anthracnose in Florida. Asian mangoes are resistant to anthracnose.[15]

The current world market is dominated by the cultivar 'Tommy Atkins', a seedling of 'Haden' that first fruited in 1940 in southern Florida and was initially rejected commercially by Florida researchers.[16] Growers and importers worldwide have embraced the cultivar for its excellent productivity and disease resistance, shelf life, transportability, size, and appealing color.[17] Although the Tommy Atkins cultivar is commercially successful, other cultivars may be preferred by consumers for eating pleasure, such as Alphonso.[14][17]

Generally, ripe mangoes have an orange-yellow or reddish peel and are juicy for eating, while exported fruit are often picked while underripe with green peels. Although producing ethylene while ripening, unripened exported mangoes do not have the same juiciness or flavor as fresh fruit.
Distribution and habitat
Mango tree in Palestine

From tropical Asia, mangoes were introduced to East Africa by Arab and Persian traders in the ninth to tenth centuries.[18] The 14th-century Moroccan traveler Ibn Battuta reported it at Mogadishu.[19] It was spread further into other areas around the world during the Colonial Era. The Portuguese Empire spread the mango from their colony in Goa to East and West Africa. From West Africa, they introduced it to Brazil from the 16th to the 17th centuries. From Brazil, it spread northwards to the Caribbean and eastern Mexico by the mid to late 18th century. The Spanish Empire also introduced mangoes directly from the Philippines to western Mexico via the Manila galleons from at least the 16th century. Mangoes were only introduced to Florida by 1833.[2][20]
Cultivation

The mango is now cultivated in most frost-free tropical and warmer subtropical climates. It is cultivated extensively in South Asia, Southeast Asia, East and West Africa, the tropical and subtropical Americas, and the Caribbean.[21] Mangoes are also grown in Andalusia, Spain (mainly in Málaga province), as its coastal subtropical climate is one of the few places in mainland Europe that permits the growth of tropical plants and fruit trees. The Canary Islands are another notable Spanish producer of the fruit. Other minor cultivators include North America (in South Florida and the California Coachella Valley), Hawai'i, and Australia.[22]

Many commercial cultivars are grafted onto the cold-hardy rootstock of the Gomera-1 mango cultivar, originally from Cuba. Its root system is well adapted to a coastal Mediterranean climate.[23] Many of the 1,000+ mango cultivars are easily cultivated using grafted saplings, ranging from the "turpentine mango" (named for its strong taste of turpentine[24]) to the Bullock's Heart. Dwarf or semidwarf varieties serve as ornamental plants and can be grown in containers. A wide variety of diseases can afflict mangoes.[citation needed]
